from flask import *


########################################################################################
# App Setup
########################################################################################
app = Flask("Opticus")
app.secret_key = "Not really very secret"


########################################################################################
# Flask Routing
########################################################################################
@app.route("/")
def root():
    return app.send_static_file('index.html')


########################################################################################
# Start Flask App
########################################################################################
if __name__ == "__main__":
    app.run(host="localhost", port=5000, debug=True)
